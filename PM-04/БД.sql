-- ============================================================
-- Представления
-- ============================================================
-- Представление: Сведенье_о_обучающиеся (без учителя, так как в Обучающихся нет поля Учитель_ID)
CREATE VIEW [dbo].[Сведенье_о_обучающиеся]
AS
SELECT 
    CONCAT(ш.Фамилия, ' ', ш.Имя, ISNULL(' ' + ш.Отчеcтво, '')) AS ФИО,
    кл.Цифра_класса AS Класс,
    COUNT(у.ID) AS [Количество участий в олимпиаде]
FROM [dbo].[Обучающиеся] ш
INNER JOIN [dbo].[Класс] кл ON ш.Класс_ID = кл.ID
LEFT JOIN [dbo].[Участие_в_олимпиаде] у ON ш.ID = у.Обучающиеся_ID
GROUP BY ш.ID, ш.Фамилия, ш.Имя, ш.Отчеcтво, кл.Цифра_класса;
GO

-- Представление: Сведенье_о_учителе
CREATE VIEW [dbo].[Сведенье_о_учителе]
AS
SELECT 
    CONCAT(уч.Фамилия, ' ', уч.Имя, ISNULL(' ' + уч.Отчество, '')) AS [ФИО учителя],
    COUNT(у.ID) AS [Количество участий в олимпиаде от учеников]
FROM [dbo].[Учитель] уч
LEFT JOIN [dbo].[Участие_в_олимпиаде] у ON уч.ID = у.Учитель_ID
GROUP BY уч.ID, уч.Фамилия, уч.Имя, уч.Отчество;
GO